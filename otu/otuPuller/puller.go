func allHexes() []string {
	horS := []string{}
	for hor := 1; hor <= 40; hor++ {
		hS := strconv.Itoa(hor)
		if hor < 10 {
			hS = "0" + hS
		}
		horS = append(horS, hS)
	}
	verS := []string{}
	for ver := 1; ver <= 32; ver++ {
		vS := strconv.Itoa(ver)
		if ver < 10 {
			vS = "0" + vS
		}
		verS = append(verS, vS)
	}
	hex := []string{}
	for _, v := range verS {
		for _, h := range horS {
			hex = append(hex, v+h)
		}
	}
	return hex
}
