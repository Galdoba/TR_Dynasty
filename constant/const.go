//WorldTypeValid - Проверка правильности написания типа Планеты
func WorldTypeValid(wType string) bool {
	allTypes := []string{
		WTpHospitable,
		WTpPlanetoid,
		WTpIceWorld,
		WTpRadWorld,
		WTpInferno,
		WTpBigWorld,
		WTpWorldlet,
		WTpInnerWorld,
		WTpStormWorld,
		WTpGG,
	}
	for i := range allTypes {
		if wType == allTypes[i] {
			return true
		}
	}
	return false
}

